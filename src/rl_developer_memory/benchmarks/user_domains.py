def seed_user_domain_memory(app: Any) -> list[dict[str, Any]]:
    results: list[dict[str, Any]] = []
    for payload in USER_DOMAIN_SEED_CASES:
        results.append(app.issue_record_resolution(**payload))
    return results


def run_user_domain_benchmark(app: Any, *, repeats: int = 1, limit: int = 3) -> dict[str, Any]:
    per_case: list[dict[str, Any]] = []
    latencies: list[float] = []
    decision_histogram: dict[str, int] = {}
    total = 0
    correct = 0
    failures: list[dict[str, Any]] = []

    for _repeat in range(max(repeats, 1)):
        for case in USER_DOMAIN_QUERY_CASES:
            start = time.perf_counter()
            result = app.issue_match(
                error_text=case.error_text,
                file_path=case.file_path,
                command=case.command,
                repo_name=case.repo_name,
                project_scope=case.project_scope,
                limit=limit,
            )
            latency_ms = (time.perf_counter() - start) * 1000.0
            latencies.append(latency_ms)
            total += 1

            decision_status = str(result['decision']['status'])
            decision_histogram[decision_status] = decision_histogram.get(decision_status, 0) + 1
            top_title = result['matches'][0]['title'] if result['matches'] else None
            ok = top_title == case.expected_title
            correct += int(ok)
            row = {
                'slug': case.slug,
                'expected_title': case.expected_title,
                'top_title': top_title,
                'decision_status': decision_status,
                'latency_ms': round(latency_ms, 3),
                'top_score': result['decision']['top_score'],
                'retrieval_event_id': result['retrieval_event_id'],
                'correct': ok,
            }
            per_case.append(row)
            if not ok:
                failures.append(
                    {
                        **row,
                        'decision': result['decision'],
                        'matches': result['matches'][:3],
                    }
                )

    mean_latency = statistics.mean(latencies) if latencies else 0.0
    median_latency = statistics.median(latencies) if latencies else 0.0
    if len(latencies) >= 20:
        p95_latency = statistics.quantiles(latencies, n=20)[18]
    elif latencies:
        p95_latency = max(latencies)
    else:
        p95_latency = 0.0

    return {
        'topics': USER_DOMAIN_LABELS,
        'seed_count': len(USER_DOMAIN_SEED_CASES),
        'query_count': len(USER_DOMAIN_QUERY_CASES),
        'repeats': max(repeats, 1),
        'total_runs': total,
        'correct_top1': correct,
        'top1_accuracy': round(correct / total, 4) if total else 0.0,
        'decision_histogram': decision_histogram,
        'latency_ms': {
            'mean': round(mean_latency, 3),
            'median': round(median_latency, 3),
            'p95': round(p95_latency, 3),
            'max': round(max(latencies), 3) if latencies else 0.0,
        },
        'failures': failures,
        'per_case': per_case,
    }
